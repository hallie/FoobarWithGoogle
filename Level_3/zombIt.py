def zombIt(food, grid):
    n = len(grid)
    new = []
    for x in range(n):
        row = []
        for y in range(n):
            z = grid[x][y]
            if (x == 0) and (y == 0):
                if (z <= food):
                    box = [z]
            elif (x == 0):
                if (z <= food):
                    box = [z + grid[0][y-1]]
            elif (y == 0):
                if (z <= food):
                    box = [z + grid[x-1][0]]
            else:
                box = []
                for e in new[x-1][y]:
                    if (z+e) <= food:
                        box.append(z+e)
                for e in row[y-1]:
                    if ((e+z) not in box) and ((e+z) <= food):
                        box.append(e+z)
            row.append(box)
        new.append(row)
    largest = -1
    for x in new[n-1][n-1]:
        if (largest < x <= food):
            largest = x
    if largest != -1:
        largest = (food - largest)
    return largest

